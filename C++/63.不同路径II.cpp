class Solution {
public:
    static long uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
        if(obstacleGrid[0][0]==1)return 0;
        int rows=obstacleGrid.size(),cols=obstacleGrid[0].size();
        if(obstacleGrid[rows-1][cols-1]==1)return 0;
        if(rows==1) {
            for (int i = 0; i < cols - 1; ++i)
                if (obstacleGrid[0][i] == 1)return 0;
            return 1;
        }
        if(cols==1) {
            for (int i = 0; i < rows - 1; ++i)
                if (obstacleGrid[i][0] == 1)return 0;
            return 1;
        }
        vector<vector<long>> a(rows,vector<long>(cols));
        for(int i=cols-2;i>=0;--i){
            if(obstacleGrid[rows-1][i]==0)
                a[rows-1][i]=1;
            else{
                for(int j=0;j<=i;++j)a[rows-1][j]=0;
                break;
            }
        }
        for(int i=rows-2;i>=0;--i){
            if(obstacleGrid[i][cols-1]==0)
                a[i][cols-1]=1;
            else{
                for(int j=0;j<=i;++j)a[i][cols-1]=0;
                break;
            }
        }
        for(int i=cols-2;i>=0;--i)
            for(int j=rows-2;j>=0;--j){
                if(obstacleGrid[j][i]==0)a[j][i]=a[j+1][i]+a[j][i+1];
                else a[j][i]=0;
            }
        return a[0][0];
    }
};

